# Exemplo de lista
my_list = [1, 2, 3, 4, 5]
print(my_list)  # List: Uma lista em Python é uma coleção ordenada de elementos,
# onde cada elemento é identificado por um índice.
# Pode conter itens de diferentes tipos e é mutável.


# Exemplo de conjunto (set)
my_set = {7, 8, 9, 10}
# Set: Um conjunto é uma coleção não ordenada de elementos únicos.
print(my_set)
# É útil para eliminar duplicatas de uma lista e realizar operações de conjuntos como união,
# interseção e diferença.


# Exemplo de dicionário (map)
my_dict = {"chave1": "valor1", "chave2": "valor2", "chave3": "valor3"}
# Map ou dicionário: Um dicionário em Python é uma coleção de pares chave-valor.
print(my_dict)
# As chaves são únicas e usadas para acessar os valores associados a elas.
